/*
 * == igual a
 * === igual extrito (valor e tipo)
 * !== diferente
 * > maior que
 * < menor que
 * >= maior igaul a
 * <= menor igual
 */

let a = 10;
let b = '10';

console.log(a == b);
console.log(a === b);
console.log(a != b);
console.log(a !== b);
console.log('');

let c = 10;
let d = 15;

console.log(c > d);
console.log(c < d);
console.log('');

let e = 9;
let f = 10;

console.log(e >= f);
console.log(e <= f);
console.log('');
